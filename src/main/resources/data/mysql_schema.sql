CREATE TABLE IF NOT EXISTS players (
    uuid CHAR(36) PRIMARY KEY,
    display_name VARCHAR(255) NOT NULL,
    game_mode ENUM('CREATIVE', 'SURVIVAL', 'ADVENTURE', 'SPECTATOR') NOT NULL,
    exp_level INT NOT NULL DEFAULT 0,
    exp_level_exp FLOAT NOT NULL DEFAULT 0,
    food_level INT NOT NULL DEFAULT 0,
    fly_mode BOOLEAN NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS server_spawn (
    name CHAR(255) PRIMARY KEY,
    world_name VARCHAR(255) NOT NULL,
    x DOUBLE NOT NULL,
    y DOUBLE NOT NULL,
    z DOUBLE NOT NULL,
    pitch FLOAT NOT NULL,
    yaw FLOAT NOT NULL
);
